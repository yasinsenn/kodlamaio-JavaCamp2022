package methods2;

public class Main {

	public static void main(String[] args) {
		
		String mesaj = "Bugün hava çok güzel";
		String yeniMesaj = sehirVer();
		System.out.println(yeniMesaj);
		
		int sayi = topla(5,7);
		System.out.println(sayi);
		
		int total = topla2(1,2,3,4,5,6,7);
		System.out.println(total);
		
	}
	
	public static void ekle() {
		System.out.println("Eklendi");
	}
	
	public static void sil() {
		System.out.println("Silindi");
	}
	
	public static void guncelle() {
		System.out.println("Güncellendi");
	}
	
	public static int topla(int sayi1, int sayi2) {
		return sayi1 + sayi2;
	}
	public static String sehirVer() {
		return "Ankara";
	}
	
	// variable argumnents (yukarýdaki topla fonksiyonunda 2 sayý ile sýnýrladýk, hesap makinesi gibi istediðimiz kadar sayý göndermek için kullanýlýr) 
	// int sonuna 3 nokta koyarak dizi gibi yaptýk bu sayede istediðimiz kadar sayý gönderebiliriz
	public static int topla2(int... sayilar) {
		int toplam = 0;
		for (int sayi : sayilar) {
			toplam += sayi; // toplam = toplam + sayi ile ayný þey
		}
		return toplam;
		
	}
	


}
